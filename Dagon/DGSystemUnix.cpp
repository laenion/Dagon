void DGSystem::browse(const char* url) {
	char aux[DGMaxFileLength];

	if (config->fullScreen) {
		XIconifyWindow(GLWin.dpy, GLWin.win, GLWin.screen);
	}

	strcpy(aux, "xdg-open ");
	strcat(aux, url);
	system(aux);
}

